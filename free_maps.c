#include "so_long.h"

void	free_maps(t_m *m)
{
	int	i;

	i = 0;
	while (i < m->row)
	{
		free(m->a[i]);
		free(m->s[i]);
		i++;
	}
	free(m->a);
	free(m->s);
	exit(0);
}
